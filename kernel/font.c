void font_load(void) {
    /* ---- Switch VGA to plane 2 (character generator) ---- */

    /* Sequencer: select plane 2 only */
    outb(0x3C4, 0x02);
    outb(0x3C5, 0x04);

    /* Sequencer: sequential access, extended memory */
    outb(0x3C4, 0x04);
    outb(0x3C5, 0x07);

    /* Graphics controller: read plane 2 */
    outb(0x3CE, 0x04);
    outb(0x3CF, 0x02);

    /* Graphics controller: disable odd/even */
    outb(0x3CE, 0x05);
    outb(0x3CF, 0x00);

    /* Graphics controller: map VGA memory to 0xA0000 */
    outb(0x3CE, 0x06);
    outb(0x3CF, 0x00);

    /* ---- Write font bitmaps ---- */
    volatile uint8_t *vga_mem = (volatile uint8_t *)0xA0000;

    for (int ch = 0; ch < 256; ch++) {
        for (int row = 0; row < 16; row++) {
            vga_mem[ch * 32 + row] = font_data[ch * 16 + row];
        }
        /* Zero pad remaining bytes (VGA uses 32 bytes per char slot) */
        for (int row = 16; row < 32; row++) {
            vga_mem[ch * 32 + row] = 0;
        }
    }

    /* ---- Restore VGA to normal text mode ---- */

    /* Sequencer: select planes 0,1 */
    outb(0x3C4, 0x02);
    outb(0x3C5, 0x03);

    /* Sequencer: odd/even addressing, normal memory */
    outb(0x3C4, 0x04);
    outb(0x3C5, 0x03);

    /* Graphics controller: read plane 0 */
    outb(0x3CE, 0x04);
    outb(0x3CF, 0x00);

    /* Graphics controller: odd/even mode */
    outb(0x3CE, 0x05);
    outb(0x3CF, 0x10);

    /* Graphics controller: map text memory to 0xB8000 */
    outb(0x3CE, 0x06);
    outb(0x3CF, 0x0E);
}

const uint8_t *font_get_data(void) {
    return font_data;
}
